package hackerrank;
import java.math.*;

public class Calculator {

    static double Add(double number,double number1)
    {
        return (number + number1);
    }
    static double Subtract(double number,double number1)
    {
        return (number - number1);
    }
    static double Divide(double number,double number1)
    {
        return (number / number1);
    }
    static double Multiply(double number,double number1)
    {
        return (number * number1);
    }

}
